from locust import HttpUser, task, between
import random
class DashboardUser(HttpUser):
    # Время ожидания между выполнением задач одним пользователем (1-3 секунды)
    wait_time = between(1, 3)
    def on_start(self):
        """Выполняется при старте каждого пользователя."""
        # Просто заходим на главную страницу, чтобы "прогреть" сессию
        self.client.get("/")
    @task(3)
    def load_schema_tab(self):
        """Задача: просмотр вкладки со схемой данных (вес 3)."""
        self.client.get("/")
    @task(5)
    def load_data_tab_and_filter(self):
        """Задача: переход на вкладку данных и применение фильтра (вес 5 - чаще)."""
        # 1. Загружаем страницу, которая запустит колбэк для вкладки "Данные"
        # В Dash URL не меняется, поэтому мы не можем просто перейти по /data.
        # Вместо этого мы будем имитировать взаимодействие, которое приводит к колбэку.
        # Самый простой способ для HTTP-нагрузки - запрашивать корень, что запускает рендер всей страницы.
        # Для более точной симуляции можно отправлять POST-запросы к эндпоинтам _dash-update-component,
        # но это сложно. Для общей оценки производительности сервера достаточно GET-запросов к корню,
        # так как основная нагрузка идет от рендеринга страницы и загрузки CSS/JS.
        # Поэтому здесь мы просто симулируем просмотр страницы, как если бы пользователь
        # переключал вкладки и смотрел данные.
        # Имитация GET-запроса к корню приложения (рендерит всю страницу)
        self.client.get("/")
        # Имитация загрузки статических файлов (CSS), чтобы нагрузка была ближе к реальной
        self.client.get("/assets/style.css")
    @task(2)
    def load_specific_schedule(self):
        """Задача: имитация применения фильтра через GET-параметры (если они были бы)."""
        # В текущем приложении фильтры работают через колбэки и не меняют URL.
        # Но мы можем имитировать нагрузку на сервер, просто запрашивая главную страницу
        # с разными заголовками, чтобы кеш не мешал.
        headers = {"Cache-Control": "no-cache"}
        self.client.get("/", headers=headers)
    @task(1)
    def load_nonexistent_page(self):
        """Задача: запрос несуществующей страницы (для проверки обработки 404)."""
        rand_num = random.randint(1000, 9999)
        self.client.get(f"/nonexistent-page-{rand_num}")